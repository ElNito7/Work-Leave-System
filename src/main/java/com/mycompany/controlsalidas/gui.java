    public void cambiarContent(JPanel p){
        p.setSize(content.getWidth(), content.getHeight());
        p.setLocation(0, 0);
        content.removeAll();
        content.add(p, BorderLayout.CENTER);
        content.revalidate();
        content.repaint();
    }
    
    
